    private void jb_transferenciaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jb_transferenciaMouseClicked
        conta = new Conta();
        Conta contaBeneficiada = new Conta();
        Conta contaProp = new Conta();
        Transferencia transf = new Transferencia();
        Reltransf relTransf = new Reltransf();
        String nome = "";
        String contaDestino = "";
        double quantia = 0;
        
        
        if(jtf_nome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "O campo de nome deve estar preenchido", "Input Error", JOptionPane.WARNING_MESSAGE);
        } else{
            contaBeneficiada.setNome(jtf_nome.getText()) ;
        }
        if(jtf_contaDestino.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "O campo numero da conta deve estar preenchido", "Input Error", JOptionPane.WARNING_MESSAGE);
        }else {
            try {
            contaBeneficiada.setNumero(jtf_contaDestino.getText());
            } catch(NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "O campo numero da conta deve ser um numero", "Input Error", JOptionPane.WARNING_MESSAGE);
            }
        }
        if(jtf_valorTransferido.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "O campo valor deve estar preenchido", "Input Error", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                quantia = Double.parseDouble(jtf_valorTransferido.getText());
                contaBeneficiada.setSaldo(quantia) ;
            } catch(NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "O campo valor deve ser um numero", "Input Error", JOptionPane.WARNING_MESSAGE);
            }
        }
        if (jcb_tipoConta.getSelectedItem().toString() != null){
        switch (jcb_tipoConta.getSelectedItem().toString()) {
        case "Conta Corrente":
            contaBeneficiada.setTipo(1);
            break;
        case "Conta Poupanca":
            contaBeneficiada.setTipo(2);
            break; 
        case "Conta Universitária":
            contaBeneficiada.setTipo(3);
            break;
        }
        
        double novoValor = clienteDAO.findById(Conta.class, 1).getSaldo() - Double.parseDouble(jtf_valorTransferido.getText());
        contaProp.setSaldo(novoValor);
        contaProp.setNumero("51456");
        contaProp.setTipo(1);
        contaProp.setId(1);
        contaProp.setNome("Luís Eduardo");
        boolean respostaProp = clienteDAO.saveOrUpdate(contaProp);
        
        transf.setValor(quantia);
        relTransf.setContaId(contaBeneficiada);
        relTransf.setTransferenciaId(transf);
        boolean resposta = clienteDAO.saveOrUpdate(contaBeneficiada);
        boolean respTransf = clienteDAOTransf.saveOrUpdate(transf);
        boolean respRel = clienteDAORelTransf.saveOrUpdate(relTransf);
        
        
        
        if(resposta && respTransf && respRel && respostaProp) 
            JOptionPane.showMessageDialog(this, "Transferencia realizada!", "Information", JOptionPane.WARNING_MESSAGE);
         else 
            JOptionPane.showMessageDialog(this, "Transferencia não realizada!", "Input Error", JOptionPane.WARNING_MESSAGE);
        
        setUltimaTransferencia();
        jlb_saldoProprietario1.setText("R$ " + novoValor);
        jtf_nome.setText("");
        jtf_contaDestino.setText("");
        jtf_valorTransferido.setText("");
    }
    }//GEN-LAST:event_jb_transferenciaMouseClicked

    private void jb_depositoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jb_depositoMouseClicked
        Conta conta = new Conta();
        Deposito deposito = new Deposito();
        Reldeposito reldeposito = new Reldeposito();
        conta = clienteDAO.findById(Conta.class, 1);
        conta.setSaldo(conta.getSaldo() + Double.parseDouble(jtf_deposito.getText()));
        boolean resposta = clienteDAO.saveOrUpdate(conta);
        
        
        deposito.setDeposito(Double.parseDouble(jtf_deposito.getText()));
        boolean respostaDeposito = clienteDAODeposito.saveOrUpdate(deposito);
        
        reldeposito.setContaId(conta);
        reldeposito.setDepositoId(deposito);
        boolean respostarel = clienteDAORelDep.saveOrUpdate(reldeposito);
        
        if(resposta && respostaDeposito && respostarel) 
            JOptionPane.showMessageDialog(this, "Deposito realizado!", "Information", JOptionPane.WARNING_MESSAGE);
         else 
            JOptionPane.showMessageDialog(this, "Deposito não realizado!", "Input Error", JOptionPane.WARNING_MESSAGE);
        
        
        
        setUltimoDeposito();
        double saldo = conta.getSaldo();
        jlb_saldoProprietario1.setText("R$ " + saldo);
        jtf_deposito.setText("");
    }//GEN-LAST:event_jb_depositoMouseClicked

